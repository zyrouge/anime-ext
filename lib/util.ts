export const functions = {
    random<T>(arr: T[]) {
        return <T>arr[Math.floor(Math.random() * arr.length)];
    },
    sleep(ms: number): Promise<void> {
        return new Promise((resolve) => {
            setTimeout(() => {
                resolve();
            }, ms);
        });
    },
    encodeURI(url: string) {
        if (url === decodeURI(url)) return encodeURI(url);
        return url;
    },
};
